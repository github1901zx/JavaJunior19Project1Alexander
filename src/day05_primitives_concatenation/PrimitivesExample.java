package day05_primitives_concatenation;

public class PrimitivesExample {
    public static void main(String[] args){
        //***** WHOLE NUMBERS/INTEGERS TYPES *****
        byte byteValue = 100;
        short shortValue = 25000;
        int intValue = 221323244;
        long longValue = 3532432434124324324L;
        //***** DECIMAL NUMBERS/FLOATING POINT TYPES *****
        float floatValue = 2345.23F;
        double doubleValue = 24567821.21321;
        //***** CHARACTER TYPE *****
        char charValue = 'C';
        //***** BOOLEAN = true|false *****
        boolean booleanValue = true; //or false
        // PRINT VARIABLE VALUES
        System.out.println(byteValue);
        System.out.println(shortValue);
        System.out.println(intValue);
        System.out.println(longValue);
        System.out.println(floatValue);
        System.out.println(doubleValue);
        System.out.println(charValue);
        System.out.println(booleanValue);



    }
}
